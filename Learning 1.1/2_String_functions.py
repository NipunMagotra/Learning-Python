
story = "Once upon a time"

#String Functions
'''len'''
print(len(story)) # we use len funtion to count the no. of strings 
'''endswith'''
print(story.endswith('time ')) #To check if the the string ends with same ending or not
'''count'''
print(story.count('e')) #Count the character or strings
'''captialize'''
print(story.capitalize()) # to captialize the first letter
'''find'''
print(story.find("a")) # To find the string at a particular position(first occurence)
'''replace'''
print(story.replace("time", "samay")) # to replace a word or character with another 
